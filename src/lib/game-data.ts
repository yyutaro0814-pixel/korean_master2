const getFixedWordsForSection = (difficulty: 'easy' | 'normal' | 'hard', sectionId: number): Question[] => {
  const masterPool = difficulty === 'easy' ? EASY_WORDS_POOL : difficulty === 'normal' ? NORMAL_WORDS_POOL : HARD_WORDS_POOL;
  
  const startIndex = (sectionId - 1) * 10;
  const slice = [];

  for (let i = 0; i < 10; i++) {
    const idx = startIndex + i;
    if (idx >= masterPool.length) break;
    
    const base = masterPool[idx];

    // 誤答を同じプールの他の意味からランダムに選ぶ
    const distractors = masterPool
      .filter(item => item.c !== base.c)
      .map(item => item.c)
      .sort(() => Math.random() - 0.5)
      .slice(0, 3);
    
    const allOptions = [...distractors, base.c].sort(() => Math.random() - 0.5);

    slice.push({
      word: base.w,
      correct: base.c,
      options: allOptions,
      example: base.e,
      exampleTranslation: base.t
    });
  }

  return slice;
};

const createSectionsForPool = (difficulty: 'easy' | 'normal' | 'hard', pool: any[]) => {
  const sectionCount = Math.floor(pool.length / 10);
  return Array.from({ length: sectionCount }, (_, i) => ({
    id: i + 1,
    words: getFixedWordsForSection(difficulty, i + 1)
  }));
};

